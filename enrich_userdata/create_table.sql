CREATE TABLE Scala (
    user_name varchar(255) NOT NULL,
    user_rank INT,
    user_type varchar(255),
    github_age INT,
    followers_num INT,
    following_num INT,
    repo_num INT,
    gist_num INT,
    location varchar(255),
    company varchar(255),
    PRIMARY KEY (user_name))
ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_bin
AUTO_INCREMENT=1 ;

